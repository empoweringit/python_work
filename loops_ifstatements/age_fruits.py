# Part 1: Stages of Life

# Set the value of the variable 'age' to represent the person's age
age = 34  # Example age

# If-elif-else chain to determine the life stage
if age < 2:
    print("The person is a baby.")
elif age < 4:
    print("The person is a toddler.")
elif age < 13:
    print("The person is a kid.")
elif age < 20:
    print("The person is a teen.")
elif age < 65:
    print("The person is an adult.")
else:
    print("The person is an elder.")

